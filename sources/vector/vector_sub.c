#include "miniRT.h"

t_vec3	vector_sub(t_vec3 v1, t_vec3 v2)
{
	t_vec3	res;

	res = vector_new(v1.x - v2.x, v1.y - v2.y, v1.z - v2.z);
	return (res);
}
